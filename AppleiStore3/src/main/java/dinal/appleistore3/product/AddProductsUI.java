    private void deleteBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBTActionPerformed
        // TODO add your handling code here:
          if (!this.isClicked) {
           errorTextPane.setText("Please select a Product"); 
           return;
        } else {
            errorTextPane.setText("");
        }
        String productID = productIdTF.getText();
        String name = nameTF.getText();
        String category = categoryCB.getSelectedItem().toString();
        try {
            int quantity = Integer.parseInt(quantityTF.getText());
            int price = Integer.parseInt(priceTF.getText());
            Product product = new Product(this.id,productID, name,category, quantity, price);
            product.delete();
            addDataToTable();
            this.clearBTActionPerformed(evt);
            errorTextPane.setText("Product has deleted");
        } catch (Exception e) {
            errorTextPane.setText("Somthing went wrong");
        }   
    }//GEN-LAST:event_deleteBTActionPerformed

    private void productTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_productTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel)productTable.getModel();
        String id = tblModel.getValueAt(productTable.getSelectedRow(), 0).toString();
        String productID = tblModel.getValueAt(productTable.getSelectedRow(), 1).toString();          
        String name = tblModel.getValueAt(productTable.getSelectedRow(), 2).toString();        
        String category = tblModel.getValueAt(productTable.getSelectedRow(), 3).toString();        
        String quantity = tblModel.getValueAt(productTable.getSelectedRow(), 4).toString();
        String price = tblModel.getValueAt(productTable.getSelectedRow(), 5).toString();
        int quantityNum = Integer.parseInt(price);
        int priceNum = Integer.parseInt(price);

        this.id = id;
        productIdTF.setText(productID);
        nameTF.setText(name);
        categoryCB.setSelectedItem(category);
        quantityTF.setText(quantity);
        priceTF.setText(price);
        this.isClicked = true;        
    }//GEN-LAST:event_productTableMouseClicked

