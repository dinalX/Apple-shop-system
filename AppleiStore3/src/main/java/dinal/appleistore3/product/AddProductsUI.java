    private void saveBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBTActionPerformed
        // TODO add your handling code here:
        String productID = productIdTF.getText();
        String name = nameTF.getText();
        String category = categoryCB.getSelectedItem().toString();
        try {
            int quantity = Integer.parseInt(quantityTF.getText());
            int price = Integer.parseInt(priceTF.getText());
            Product product;
            if (this.isClicked) {
                product = new Product(this.id,productID, name,category, quantity, price);
                product.update();
            } else {
                product = new Product(productID,name,category,quantity,price);
                product.save();
            }
            addDataToTable();
            this.clearBTActionPerformed(evt);
            errorTextPane.setText("Product has saved");
        } catch (Exception e) {
            errorTextPane.setText("Please enter a valid data");
        }
    }//GEN-LAST:event_saveBTActionPerformed

